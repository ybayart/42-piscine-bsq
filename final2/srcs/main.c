#include "bsq.h"

int		main(int argc, char **argv)
{
	int		i;

	i = 1;
	if (argc > 1)
		while (i < argc)
		{
			new_plate(argv[i], i);
			i++;
		}
	else
		new_plate(0, 0);
	return (0);
}
